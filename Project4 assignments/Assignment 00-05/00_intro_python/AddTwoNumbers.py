"""
Write a Python program that takes two integer inputs from the user
and calculates their sum.
"""

# Prompt the user to enter the first number
first_integer = int(input("Enter first number: "))

# Prompt the user to enter the second number
second_integer = int(input("Enter second number: "))

# Calculate the sum of the two numbers
sum_of_numbers = first_integer + second_integer

# Print the total sum with an appropriate message
print(f"The sum of {first_integer} and {second_integer} is {sum_of_numbers}")

